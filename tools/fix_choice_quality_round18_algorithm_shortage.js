let changed = 0;

function replaceDistractors(question, distractors) {
  const escaped = question.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const pattern = new RegExp(
    `("${escaped}"\\s*:\\s*\\{[\\s\\S]*?"correct"\\s*:\\s*"[^"]+"\\s*,\\s*"distractors"\\s*:\\s*)\\[[\\s\\S]*?\\]`,
    "m"
  );
  const replacement = `$1${JSON.stringify(distractors, null, 6).replace(/\n/g, "\n    ")}`;
  const next = source.replace(pattern, replacement);
  if (next === source) {
    throw new Error(`Question not found or not updated: ${question}`);
  }
  source = next;
  changed += 1;
}

for (const [question, distractors] of updates) {
  replaceDistractors(question, distractors);
}

if (write) {
  fs.writeFileSync(target, source);
}

console.log(JSON.stringify({ changed, write }, null, 2));
